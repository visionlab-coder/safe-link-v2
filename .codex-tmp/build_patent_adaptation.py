def font_run(run, size=None, bold=None, color=None):
    run.font.name = "Malgun Gothic"
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "Malgun Gothic")
    if size:
        run.font.size = Pt(size)
    if bold is not None:
        run.bold = bold
    if color:
        run.font.color.rgb = RGBColor(*color)


def shade(cell, fill):
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:fill"), fill)
    tc_pr.append(shd)


def cell_text(cell, text, bold=False, fill=None, white=False):
    cell.text = ""
    p = cell.paragraphs[0]
    r = p.add_run(text)
    font_run(r, 9, bold, (255, 255, 255) if white else None)
    cell.vertical_alignment = WD_CELL_VERTICAL_ALIGNMENT.CENTER
    if fill:
        shade(cell, fill)


def heading(doc, text, level=1):
    p = doc.add_heading(text, level=level)
    for r in p.runs:
        font_run(r)
    return p


def para(doc, text="", style=None):
    p = doc.add_paragraph(style=style)
    r = p.add_run(text)
    font_run(r, 10)
    p.paragraph_format.space_after = Pt(6)
    p.paragraph_format.line_spacing = 1.15
    return p


def bullets(doc, items):
    for item in items:
        p = doc.add_paragraph(style="List Bullet")
        r = p.add_run(item)
        font_run(r, 10)
        p.paragraph_format.space_after = Pt(3)


def numbered(doc, items):
    for item in items:
        p = doc.add_paragraph(style="List Number")
        r = p.add_run(item)
        font_run(r, 10)
        p.paragraph_format.space_after = Pt(3)


def claim(doc, num, title, body):
    heading(doc, f"청구항 {num}. {title}", 3)
    for line in body:
        para(doc, line)


